export function getLocale() {
  const raw = window.app?.locale || document.documentElement.lang || '';
  if (raw === 'zh-CN' || String(raw).startsWith('zh')) return 'zh-CN';
  return 'en-US';
}

export function translate(key, params = {}) {
  const table = STRINGS[getLocale()] || STRINGS['en-US'];
  const fallback = STRINGS['en-US'][key] || key;
  let value = table[key] || fallback;
  Object.entries(params).forEach(([name, replacement]) => {
    value = value.replaceAll(`{{${name}}}`, String(replacement));
  });
  return value;
}
